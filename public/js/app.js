var app = angular.module('Parlay', ['ngRoute']);

app.config(function($routeProvider){
  $routeProvider
    .when('/', {
      controller: '',
      templateUrl: ''
    })
    .when('/', {
      controller: '',
      templateUrl: ''
    })
    .when('/', {
      controller: '',
      templateUrl: ''
    })
    .otherwise({
      redirectTo: '/'
    })




})
